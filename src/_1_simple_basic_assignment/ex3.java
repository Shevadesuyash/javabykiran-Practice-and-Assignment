package _1_simple_basic_assignment;

public class ex3 {

	public static void main(String[] args) {
		int num;
		float b;
		char c;
		String s;
		// integer
		num = 30;
		// float
		b = 1.234f;
		// character
		c = 'S';
		// string
		s = "Hello Java";
		System.out.println("Value of num: " + num);
		System.out.println("Value of b: " + b);
		System.out.println("Value of c: " + c);
		System.out.println("Value of s: " + s);
	}
}
